La Universidad Mariano Gálvez es una institución de educación superior privada de Guatemala fue aprobada como tal el 29 de enero de 1996 por el Consejo Supremo Universitario de la Universidad de San Carlos de Guatemala.<br>
Su nombre hace honor al eximio estadista prócer y jurisconsulto Doctor José Mariano Gálvez (Jefe del Estado de Guatemala 1831 - 1838) fundador de la Academia de Estudios y reformador de la educación guatemalteca normal.<br>
Las primeras instalaciones utilizadas por la Universidad fueron ambientes del edificio de la Antigua Escuela de Enfermeras del Hospital Americano en la ciudad capital e iniciando su traslado en el año 1972 hacia el Campus Central actual. <br>
Las ideales se mantienen vigentes, pues es preocupación de la Universidad en su quehacer docente, la formación integral de sus estudiantes, en un ambiente de libertad, respeto mutuo y espíritu de convivencia.<br><br>

<h2>Reseña Histórica de la Universidad Mariano Gálvez extensión Quiché</h2>
La Universidad Mariano Gálvez de Santa Cruz del Quiché, nació como una necesidad de profesionales con iniciativa y aspiraciones a una superación y grado académico que les permitiera desenvolverse en diferentes campos profesionales con el fin de servir al pueblo quichelense, y así también brindarles a las personas que tienen el deseo de superación, la oportunidad de crecimiento profesional en cualquiera de las carreras que ofrece el Centro Universitario, beneficiándolos en el sentido de no viajar hasta la Ciudad Capital para su superación. <br>
Haciendo énfasis que su primordial fundación nació con el sueño de un grupo de profesionales del nivel medio del Colegio Evangélico Metodista Utatlán que aspiraban estudiar la carrera de Ciencias Jurídicas y Sociales en el año de 1996, idea que se fue madurando por un plazo de 3 años ya que se buscaron universidades que aceptaran el reto de iniciar la Universidad en Santa Cruz del Quiché, y el único establecimiento que aceptó el reto fue la Universidad Mariano Gálvez debido a que este centro de educación superior era compatible con los valores del Colegio Evangélico Metodista Utatlán.<br> Dentro de los fundadores de la Universidad Mariano Gálvez de Santa Cruz del Quiché resaltan el Licenciado Juan Par Guits, Marco Antonio Rivera Orrego y el Licenciado Jacob Girón Alegría, actualmente director del Centro Universitario por su dedicación y servicios profesionales brindados al Centro.<br>

<h3>Misión</h3>
Formar, en niveles de excelencia académica y de acuerdo con las necesidades y demandas de la sociedad, profesionales, investigadores, técnicos y docentes científicamente competentes, éticamente responsables y socialmente abiertos al cambio progresista, mediante el desarrollo integrado de la docencia, la investigación y el servicio a la comunidad, a fin que ésta tenga la oportunidad de participar de los beneficios de la ciencia, la tecnología y la cultura.
Asimismo, que sus graduados sean capaces de conservar, acrecentar y difundir la cultura universal, afirmando los valores propios e inmersos en el proceso productivo del país, y que contribuyan con espíritu crítico a la solución de los problemas nacionales.

<h3>Visión</h3>
Ser una institución de educación superior altamente competitiva, líder en su género, innovadora y creadora de conocimientos científicos y tecnológicos, en la que sus educandos alcancen una formación integral, armónica y potencien sus aptitudes y capacidades, en un ambiente de libertad y aceptación de los supremos valores del cristianismo.
Y que sus egresados, sean capaces de asumir los desafíos del mundo contemporáneo y de una sociedad cambiante, comprometidos con el desarrollo económico, social, político y cultural del país, en permanente interacción con el entorno nacional, con renovada vocación de servicio y espíritu de solidaridad.
